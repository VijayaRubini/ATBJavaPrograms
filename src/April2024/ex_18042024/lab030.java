package April2024.ex_18042024;

public class lab030 {
    public static void main(String[] args) {
        //compound assignment operators
        int a=10;
        int b=45;

        //+=,-=,*=,/=,%=

        b+=30; //b=b+30
        System.out.println(b);

        //int a=50;
        a-=28;
        System.out.println(a);

        int c=5;
        c*=67;
        System.out.println(c);

        int d=80;
        d/=4;
        System.out.println(d);

        int e=95;
        e%=30;
        System.out.println(e);





    }
}
