package April2024.ex_27042024;

public class lab115 {
    public static void main(String[] args) {
        //while loop
        System.out.println("*****using While loop******");
        int i=1;
        while(i<=10)
        {
            System.out.println(i);
            //execute code
            i++;

        }

        System.out.println("*********using for loop*********");
        for(int j=1;j<=10;j++)
        {
            System.out.println(j);
        }
    }
}
