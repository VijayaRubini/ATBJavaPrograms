public class lab085 {
    public static void main(String[] args) {
        byte b=20;
        switch(b){
            case 10:
                System.out.println("TEN");
                break;
            case 127:
                System.out.println("One Twenty Seven");
                break;

            default:
                System.out.println("Default");
        }
    }
}
